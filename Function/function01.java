class function01
{
	public static void main(String args[])
	{
		int x=10;
		int y=2;

		int result=0;

		if(x > y)
		 result = x/y;
		else if(x < y)
		  result = x+y;
		else
		  result = x*y;

		System.out.println("Result=" +result);	

		x = 10;
		y = 50;
	if(x > y)
		 result = x/y;
		else if(x < y)
		  result = x+y;
		else
		  result = x*y;

		System.out.println("Result=" +result);	


	}
}