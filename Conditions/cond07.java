class cond07
{
public static void main(String args[])
	{
		int a=Integer.parseInt(args[0]);
		//switch case
			switch(a)
			{
				case 1:
				System.out.println("One");
				break;
				case 2:
				System.out.println("Two");
				break;
				default:
				System.out.println("error!");
			}
	}
}